import pytest
from docx import Document

pytestmark = pytest.mark.unit


def test_quality_gate_blocks_raw_markdown_and_prohibited_defaults(sample_params_depository):
    from src.structured_blueprint import normalise_structured_result, run_quality_gate

    raw = {
        "summary": "## Summary\n\nUse default value 'Unknown' for mandatory account holder name.",
        "architecture": "### Architecture\n\nUS place of birth should not be in CRS logic.",
        "field_catalog": "| Field | Rule |\n|---|---|\n| Name | Use default values |",
        "downstream": "Portal details are [INFERRED].",
        "risk_flags": "1. Missing self-certification.",
        "classification": "Renew every 3 years.",
        "governance": "Use last known values.",
        "testing": "No TIN scenario.",
    }
    structured = normalise_structured_result(raw, sample_params_depository, {})
    quality = run_quality_gate(structured)
    all_text = str(structured).lower()

    assert quality["passed"]
    assert "use default value 'unknown'" not in all_text
    assert "use default values" not in all_text
    assert "use last known values" not in all_text
    assert "renew every 3 years" not in all_text
    assert "local confirmation required" in all_text


